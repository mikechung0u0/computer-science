#mathematical operations in python

#addition
computer_science_students = 21
business_students = 17
total_students = computer_science_students + business_students
print("my IGCSE student total = ", total_students)

#subtraction
highest_score = 100
my_score = 57
difference = highest_score - my_score
print("highest score - my score = ", difference)

#multiplication
siblings = 4
number_of_children_per_sibling = 3
family_size = siblings * number_of_children_per_sibling
print("total family size = ", family_size)

#division
money_available = 100
cost_per_ice_cream = 100
total_ice_cream_possible_to_buy = money_available / cost_per_ice_cream
print("You can buy = ", total_ice_cream_possible_to_buy, "ice cream")

#modulus operator - gives you the remainder left over after a division
x = 20
y = 5
remainder_after_division = x % y 
print("Remainder after the division = ", remainder_after_division)

#addition
#there are 5000 cows, 2000 chicken, 188 sheep in a farm
#how many total animals?
cows = 5000
chicken = 2000
sheep = 188 
total = cows + chicken + sheep
print("animals before slaughter", total)
#subtraction
#if 78 sheep are killed and 230 cows are also killed 
#how many are total animals are left?

killed_cows = 230
killed_sheep = 78
total_after_slaughter = total - (killed_cows + killed_sheep)
